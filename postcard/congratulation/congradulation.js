const images1 = [
    "../pictures/free-icon-gift-5990435.png",
    "../pictures/free-icon-gift-box-837993.png",
];

let currentIndex = 0;
let isForward = true;

setInterval(function changeImage1() {
    const mainImageElement = document.getElementById("image1");
    mainImageElement.src = images1[currentIndex];
    if (isForward) {
        currentIndex++;
    } else {
        currentIndex--;
    }

    if (currentIndex === images1.length) {
        currentIndex = images1.length - 2;
        isForward = false;
    } else if (currentIndex === -1) {
        currentIndex = 1;
        isForward = true;
    }
}, 1000);

